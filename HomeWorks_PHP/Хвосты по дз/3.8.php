Сделать так, чтобы в конце строки гарантированно стояла точка. 
То есть: если точки нет - ее надо добавить. Если есть - ничего не делать.
Задачу решить через rtrim.*/

/*Даша, я приношу свои извинения, но я не совсем поняла, как решить эту задачу ОДНОВРЕМЕННО 
и через rtrim, и с условием "если точки нет - ее надо добавить. Если есть - ничего не делать.".
Кроме того, совсем не поняла, зачем использовать функцию удаления символов из конца строки, если
