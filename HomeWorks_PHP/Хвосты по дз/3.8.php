//Решение через условие "если точки нет - ее надо добавить. Если есть - ничего не делать."
$str = 'просто строка';
if (!strpos($str, '.')) {
    $str = str_replace('строка', 'строка.', $str);
}
echo($str);

//Решение с использованием функции rtrim
$string = 'просто строка';
if (strpos($string, '.')) {
    $string = rtrim($string, '.');
}
$string = str_replace('строка', 'строка.', $string);
echo "<br>" . $string;